public class MeinDrittesProgramm {
    public static void main(String[] args) {
        // Dekleration
        int grundseite;
        int hoehe;
        // Initialisierung (erstmalige Wertzuweisung) bzw. Wertzuweisung
        grundseite = 6;
        hoehe = 4;
        // Rechenen
        int ergebnis;
        ergebnis = grundseite*hoehe/2;
        // Ausgabe des Ergebnisses
        System.out.println("Das Ergebnis ist g*h/2");
        System.out.println(grundseite +" * " +hoehe +" / 2 = "+ ergebnis);
    }
}
